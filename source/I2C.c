void I2C_Init()
{
  // Clock gating
  SIM->SCG1 |= SIM_SCGC1_I2C2(HIGH);
  SIM->SCG1 |= SIM_SCGC4_I2C1(HIGH) | SIM_SCGC4_I2C0(HIGH);


  // Port configuration
  SIM->SCG5 |= SIM_SCG5_PORTA(HIGH) | SIM_SCG5_PORTB(HIGH) | 
              SIM_SCG5_PORTC(HIGH) | SIM_SCG5_PORTD(HIGH) | SIM_SCG5_PORTE(HIGH);
}

